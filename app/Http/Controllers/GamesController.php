     */
    public function store(Request $request)
    {
        $this->validate($request, array(
            'title' => 'required',
            //  'company' => 'required'
        ));

        $game = new Game;

        $game->title = $request->title;
        //$game-> company = $request->company;

        $game->save();

        return redirect('games');
    }

